      PARAMETER(C1M1=1.0D-1,C1M3=1.0D-3,C1M6=1.0D-6,C1M7=1.0D-7)
      PARAMETER(C1M8=1.0D-8,C1M9=1.0D-9,C1M10=1.0D-10)
