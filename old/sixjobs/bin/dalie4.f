      CALL FEXPO(B9,W,X,KP,KP,ONE,1)
* B6 IS THE NEW CONTRIBUTION TO FT
      CALL DAADD(B6,FT,B1)
      CALL DACOP(B1,FT)
 33   CONTINUE
      CALL DADAL(B9,1)
      CALL DADAL(B6,1)
      CALL DADAL(B5,1)
      CALL DADAL(B1,1)
      RETURN
      END
